<p> Fruit: </br>
Guava fruits, usually 4 to 12 centimetres (1.6 to 4.7 in) long, are round or oval depending on the species. They have a pronounced and typical fragrance, similar to lemon rind but less sharp. The outer skin may be rough, often with a bitter taste, or soft and sweet. Varying between species, the skin can be any thickness, is usually green before maturity, but becomes yellow, maroon, or green when ripe. The pulp inside may be sweet or sour, and off-white ("white" guavas) to deep pink ("red" guavas). The seeds in the central pulp vary in number and hardness, depending on species. </p>
